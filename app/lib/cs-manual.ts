// 매뉴얼 항목 조회 — 비어 있으면 기본 매뉴얼로 자동 시드.
export async function fetchManualEntries(): Promise<CsManualEntry[]> {
  const sb = supabaseAdmin();
  const { data, error } = await sb
    .from("cs_manual")
    .select("*")
    .order("sort_order", { ascending: true })
    .order("created_at", { ascending: true });
  if (error) throw error;

  if (!data || data.length === 0) {
    // 첫 사용 시 기본 매뉴얼 시드
    const { data: seeded, error: seedErr } = await sb
      .from("cs_manual")
      .insert(DEFAULT_CS_MANUAL)
      .select("*");
    if (seedErr) {
      // 시드 실패해도 조회는 막지 않음(동시 시드 충돌 등) — 다시 한 번 읽어봄
      const retry = await sb.from("cs_manual").select("*").order("sort_order", { ascending: true });
      return (retry.data ?? []) as CsManualEntry[];
    }
    return ((seeded ?? []) as CsManualEntry[]).sort((a, b) => a.sort_order - b.sort_order);
  }

  return data as CsManualEntry[];
}

// 항목들을 시스템 프롬프트에 넣을 매뉴얼 텍스트로 조립.
export function assembleManual(entries: { title: string; content: string }[]): string {
  if (entries.length === 0) return "(등록된 매뉴얼이 없습니다.)";
  return entries
    .map((e) => `[${e.title}]\n${e.content}`.trim())
    .join("\n\n");
}
